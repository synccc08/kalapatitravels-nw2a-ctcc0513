    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
//        store the input in this variable
        String username = jTextField2.getText();
//        the query to be executed later on
        String usernameQuery
                = "SELECT Name, Address, Number, Occupation, Age, Nationality, Destination, Sex, Seat "
                + "FROM traveler "
                + "WHERE Name = ?";
        try {
//            jdbc driver
            Class.forName("com.mysql.cj.jdbc.Driver");
//      link
            String url = "jdbc:mysql://localhost:3307/KalapatiTravels";
// db  login
            String user = "root";
            String password = "Cmmd10082003";
            Connection connection = DriverManager.getConnection(url, user, password);

            PreparedStatement usernameStatement = connection.prepareStatement(usernameQuery);
//          will use the values stored in username variable
            usernameStatement.setString(1, username);

//            execute
            ResultSet usernameResultSet = usernameStatement.executeQuery();

            if (usernameResultSet.next()) {
//                gets all the info and passes it into TravelerUI.java
                String name = usernameResultSet.getString("Name");
                String address = usernameResultSet.getString("Address");
                String number = usernameResultSet.getString("Number");
                String occupation = usernameResultSet.getString("Occupation");
                int age = usernameResultSet.getInt("Age");
                String nationality = usernameResultSet.getString("Nationality");
                String destination = usernameResultSet.getString("Destination");
                String sex = usernameResultSet.getString("Sex");
                int seat = usernameResultSet.getInt("Seat");
                TravelerUI TravelerUI = new TravelerUI(name, address, number, occupation, age, nationality, destination, sex, seat);
                TravelerUI.show();

            } else {
//                if username doesn't exist, prompt
                JOptionPane.showMessageDialog(null, "Username does not exist.");
            }

            usernameResultSet.close();
            usernameStatement.close();
            connection.close();
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

